<h2 id="system-wspomagaj-cy-losowanie">System wspomagający losowanie</h2>
<p>Elementy wspomagania, czyli przebieg losowania z punktu widzenia kolejnych kroków wykonywanych przez operatora systemu wspomagania losowania - <em>Panel Helper</em>.</p>
<h3 id="krok-przygotowanie-do-losowania">Krok 1. Przygotowanie do losowania</h3>
<p>Podczas przygotowania należy ustalić, które kryteria mogą zostać spełnione w trakcie losowania.</p>
<p>Aby to ułatwić system prezentuje dwie wizualizacje (dla porównania):
<ul>
	<li>Teoretycznego rozkładu (opracowanego na podstawie statystyk).</li>
	<li>Rzeczywistego rozkładu (z danych profilowych z ankiet).</li>
</ul>
<p>Operator decyduje jakie kryterie zostaną użyte przy losowaniu i tworzy profile statystyczne używane później przy losowaniu.</p>
<h3 id="krok-losowanie-grupy-g-wnej-i-zast-pczej">Krok 2. Losowanie grupy głównej i zastępczej</h3>
<p>Ten etap powienien być transmitowany na żywo, aby uwiarygodnić losowanie.</p>
<ol>
	<li>Operator wprowadza profil jako kryteria wyszukiwania. Operator ma swobodę rezygnacji z dowolnych kryteriów (ignorowania ich).</li>
	<li>Wyświetlane są wszystkie dane dopasowane do wybranego profilu statystycznego.</li>
	<li>Operator może zmienić kryteria lub ponownie wylosować profil w wypadku zbyt małej liczby osób do przeprowadzenia losowania.</li>
	<li>Operator przenosi wylosowaną osobę do grupy głównej.</li>
	<li>Z tej samej listy losowana jest przynajmniej 1 osoba do grupy zastępczej.</li>
</ol>
<p>Dane kontaktowe są widoczne dopiero na koniec i tylko dla operatora. Jedynie dana osoba może zweryfikować kiedy została wylosowana (zna swój identyfikator).</p>
<h3 id="krok-wyniki">Krok 3. Wyniki</h3>
<p>Ten etap może być transmitowany na żywo jeśli losowane osoby wyraziły w ankietach zgodę na ujawnienie imienia i nazwiska.</p>
<p>System jest przełączany w tryb wyników przez administratora na prośbę operatora.
Blokowana jest przy tym możliwość zmiany grupy, a staje się możliwe wyświetlenie listy wylosowanych.</p>
<p>Na listach domyślnie widoczna są tylko imioniona i nazwiska posortowane alfabetycznie.</p>
<p>Jeśli ten etap jest transmitowany, to transmisję należy zakończyć przed odkryciem danych kontaktowych.</p>